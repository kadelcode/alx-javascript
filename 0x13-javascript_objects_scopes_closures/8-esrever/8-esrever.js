exports.esrever = function (list) {
  // a function that returns the reversed version of a list

  // create an empty list
  const revList = [];

  // let listSize = list.length - 1;
  for (let i = list.length - 1; i => 0; i--) {
    revList.push(list[i]);
  }

  return revList;
};
